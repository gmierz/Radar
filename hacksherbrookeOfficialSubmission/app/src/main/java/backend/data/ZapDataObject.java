package backend.data;

/**
 * This is a basic data object
 * 
 * for Zap location data.
 * 
 * */
public class ZapDataObject extends AbstractDataObject {
	private String id;
	private String name;
	private String description;
	private String civic_number;
	private String street_name;
	private String city;
	private String province;
	private String country;
	private String postal_code;
	private String public_phone_number;
	private String public_email;
	private String latitude;
	private String longitude;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCivic_number() {
		return civic_number;
	}
	public void setCivic_number(String civic_number) {
		this.civic_number = civic_number;
	}
	public String getStreet_name() {
		return street_name;
	}
	public void setStreet_name(String street_name) {
		this.street_name = street_name;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getPostal_code() {
		return postal_code;
	}
	public void setPostal_code(String postal_code) {
		this.postal_code = postal_code;
	}
	public String getPublic_phone_number() {
		return public_phone_number;
	}
	public void setPublic_phone_number(String public_phone_number) {
		this.public_phone_number = public_phone_number;
	}
	public String getPublic_email() {
		return public_email;
	}
	public void setPublic_email(String public_email) {
		this.public_email = public_email;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	
}
