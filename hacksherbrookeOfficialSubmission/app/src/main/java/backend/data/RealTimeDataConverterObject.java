package backend.data;

public class RealTimeDataConverterObject {
	public EventDataObject toRealTimeData(RealTimeEventsDataObject Eo){
				
		EventDataObject Edo = new EventDataObject();
		Edo.setAddress(Eo.getAD_NO() + Eo.getAD_LIEN() + Eo.getAD_GEN() +  Eo.getAD_MUN() + Eo.getCATEG() );
		
		return Edo;
		}	
	

}
